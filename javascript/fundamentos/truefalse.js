// Valores falsy:

// false
// 0
// '' (cadena vacía)
// null
// undefined
// NaN
// Valores truthy:

// Cualquier valor no falso (es decir, cualquier valor diferente a los valores falsy mencionados anteriormente), como:
// true
// Cualquier número diferente de cero (positivo o negativo)
// Cualquier cadena de texto no vacía (incluyendo espacios en blanco)
// Cualquier objeto (incluyendo objetos vacíos)
// Cualquier arreglo (incluyendo arreglos vacíos)
// Cualquier función (incluyendo funciones vacías)
// Infinity y -Infinity

// Es importante tener en cuenta que el concepto de valores truthy y falsy
// se utiliza en muchas partes de JavaScript, como en las condiciones (if, while, etc.),
// en la evaluación de expresiones lógicas (&&, ||), en la conversión implícita de tipos de
// datos y en otras operaciones.
